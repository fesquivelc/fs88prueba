    public enum TipoOperacion {
        CAPTURAR, ENROLAR, IDENTIFICAR, VERIFICAR
    }

    private class HiloOperacion implements Runnable {

        private TipoOperacion operacion;

        public HiloOperacion(TipoOperacion operacion) {
            this.operacion = operacion;
        }
        
        @Override
        public void run() {
            while (activo) {
                switch (operacion) {
                    case CAPTURAR:
                        obtenerCaptura();
                }
            }
        }

        private void obtenerCaptura() {
            IntByReference equipo = ftrScanAPI.ftrScanOpenDevice();
            FTRScanAPI.FTRSCAN_IMAGE_SIZE.ByReference imageSize = new FTRScanAPI.FTRSCAN_IMAGE_SIZE.ByReference();
            ftrScanAPI.ftrScanGetImageSize(equipo, imageSize);
            byte[] imagen = new byte[imageSize.nImageSize];
            boolean captura = ftrAnsiSDK.ftrAnsiSdkCaptureImage(equipo, imagen);
            if (captura) {
                iconoHuella.setImage(byteToImagen(imageSize.nWidth, imageSize.nHeight, imagen));
                lblHuella.repaint();
            } 
        }

    }

    public Image byteToImagen(int ancho, int alto, byte[] pBitmap) {
        BufferedImage hImage = new BufferedImage(ancho,
                alto,
                BufferedImage.TYPE_BYTE_GRAY);
        DataBuffer db1 = hImage.getRaster().getDataBuffer();
        for (int i = 0; i < db1.getSize(); i++) {
            db1.setElem(i, pBitmap[i]);
        }
        return hImage;
    }

