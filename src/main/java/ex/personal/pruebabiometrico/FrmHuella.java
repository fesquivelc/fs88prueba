    private ImageIcon createImageIcon(String path) {
        File fichero = new File(path);
        if (fichero.exists()) {
            return new ImageIcon(fichero.getAbsolutePath());
        } else {
            System.err.println("Couldn't find file: " + path);
            return null;
        }
    }

